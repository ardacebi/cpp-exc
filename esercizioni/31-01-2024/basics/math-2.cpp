#include <iostream>
#include <cmath> // the c++ mathematics library is imported as cmath

using namespace std;

/* MATHEMATICAL OPERATORS
Math operations require the importing of the cmath library (line 2).
While sqrt takes the square root of 64 (x), round rounds the number to the nearest one.
*/

int main() {
    int x = 64;
    cout << sqrt(x) << endl;
    cout << round(2.6) << endl; 
    return 0;
}